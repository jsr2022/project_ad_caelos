    def get_state_position_2_names(self, indices = None):
        if indices is None:
            return np.array(list(self.__state_position_2_names.values()))
        elif isinstance(indices, int):
            if indices not in self.__state_position_2_names:
                raise KeyError(f"indices {indices} not found in dictionary")
            return list(self.__state_position_2_names[indices])
        elif isinstance(indices, np.ndarray): 
            stateNames = []
            for key in indices:
                if key not in self.__state_position_2_names:
                    raise KeyError(f"Key {key} not found in dictionary")
                stateNames.append(self.__state_position_2_names[key])
            return stateNames
        else:
            print("Error: Improper Key Type Not In Dictionary") #add indices type, also add to logger
            sys_exit(1)

    def get_variable_names_2_position(self, indices = None) -> list:
        if indices is None:
            return list(self.__variable_names_2_position.values())
        elif isinstance(indices, str): 
            statePos = []
            for key in indices:
                if key not in self.__variable_names_2_position:
                    raise KeyError(f"Key {key} not found in dictionary")
                statePos.append(self.__variable_names_2_position[key])
            return statePos
        else:
            print("Error: Improper Key Type Not In Dictionary") #add indices type, also add to logger
