    def initialize_data_storage(self, next_time: float, variable_names: str | list[str], variable_initial_conditions: np.array) -> None:
        """
        _Populates the keys for the data storage dictionary. Should be called at the end of the constructor after all necessary attributes have been defined._

        Parameters
        ----------
        variable_names : str or list of str
            The name(s) of the variable(s) for which to initialize data storage.
        """
        if isinstance(variable_names, str):
            variable_names = [variable_names]
        
        counter = 0
        self.__data_storage[0] = arr.array('d',[next_time])  # Initialize time storage with the initial time
        for name in variable_names:
            self.__data_storage[name] = arr.array('d',[variable_initial_conditions[counter]])  # Initialize with an empty array of doubles
            counter+=1
            
    def store_data(self, current_time: float, data: np.array) -> None:
        """
        Stores data generated by this component.

        Parameters
        ----------
        current_time : float
            The current simulation time at which the data is being stored [s].
        data : np.array 
            numpy array of state data stored where each index + 1 corresponds to the state index in the state vector. 
            this corresponds to the keys of the state_position_2_names dictionary which maps state indices to state names.
        """
        
        if data.size != len(self.__state_position_2_names):
            raise ValueError(f"Data size {data.size} does not match the number of states with names {len(self.__state_position_2_names)}")
        self.__data_storage[0].append(current_time) # Store the current time in the time storage list
        for i in np.arange(1, data.size + 1): #the first index is for time!
            self.__data_storage[i].append(data[i])
        
        self.num_steps_stored += 1
            
    
    def get_all_stored_data(self) -> dict:
        """
        Returns data storage dictionary

        Returns
        -------
        dict
            _Key is 0 through number of time + states and each value is a python array of floats of corresponding state data (or time)_
        """
        return self.__data_storage
    
